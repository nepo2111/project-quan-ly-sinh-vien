    private void hienThiChiTietDiemThi() {
        //Khai báo một đối tượng 
        DiemThiBusiness diemThiBus = new DiemThiBusiness();
        
        DiemThi objDiemThi = diemThiBus.layChiTietTheoMa(sinhVienId);

        //Hiển thị lên frm
        txtHoTen.setText(objDiemThi.getHoTen());
        txtDiemThi.setText(objDiemThi.getDiemThi() + "");
        jDateChooser1.setDate(objDiemThi.getNgayThi());
        
        //Xử lý chọn Combo box của sinh viên trong danh sách hiển thị lên combobox
        if (!objDiemThi.getSinhVienId().isEmpty()) {
            IDSinhVienBusiness IdBus = new IDSinhVienBusiness();
            
            IDSinhVien objID = IdBus.layChiTietTheoMa(objDiemThi.getSinhVienId());
            if (objID != null) {
                cboIdSV.getModel().setSelectedItem(objID);
            }
        }    
            //Xử lý chọn môn học
            if (!objDiemThi.getMonHocId().isEmpty()){
            MonHocBusiness monHocBus = new MonHocBusiness();     
            
            MonHoc objMonHoc = monHocBus.layChiTietTheoMa(objDiemThi.getMonHocId());
            if (objMonHoc != null) {
                cboMonHoc.getModel().setSelectedItem(objMonHoc);
            }
            }
            
            if (!objDiemThi.getPhongHocId().isEmpty()){
            //xử lý phòng học
            PhongHocBusiness phongHocBus = new PhongHocBusiness();
            
            PhongHoc objPhongHoc = phongHocBus.layChiTietTheoMa(objDiemThi.getPhongHocId());
            if (objPhongHoc != null) {
                cboPhongThi.getModel().setSelectedItem(objPhongHoc);
            }   
    }
    }      
    private void hienThiDanhSachMonHoc() {
        ///Khai báo 1 đối tượng
        MonHocBusiness monHocBus = new MonHocBusiness();
        //Lấy danh sách khoa
        List<MonHoc> lstMonHoc = monHocBus.layDanhSachMonHoc();
        MonHoc root = new MonHoc();
        root.setMaMH("");
        root.setTenMonHoc("---Chọn môn---");
        lstMonHoc.add(0, root);
        //Khai báo 1 đối tượng để hiển thị thông tin lên combobox
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        //Duyệt để đưa vào danh sách
        for(MonHoc o: lstMonHoc)
        {
            model.addElement(o);
        }
        //Hiển thị lên combobox
        cboMonHoc.setModel(model);
        //Tuỳ biến lại từng item của combobox
        cboMonHoc.setRenderer(new MonHocRender());
    }
    
