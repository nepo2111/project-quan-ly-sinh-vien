    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        //Khai báo đối tượng
        DiemThiBusiness diemThiBus = new DiemThiBusiness();
        DiemThi objDiemThi = new DiemThi();
        //Khai báo biến
        String maId = "", hoTen = "", strDiemThi = "", monHocId = "", phongHocId = "";
        double diemThi = 0;
        Date ngayThi;     
        //Lấy dữ liệu từ giao diện
        ngayThi = jDateChooser1.getDate();
        hoTen = txtHoTen.getText();
        strDiemThi = txtDiemThi.getText();
        
        //Lấy Id môn học
        MonHoc objMonHoc = (MonHoc) cboMonHoc.getSelectedItem();
        monHocId = objMonHoc.getId();
        
        //Lấy Id phòng
        PhongHoc objPhongHoc = (PhongHoc) cboPhongThi.getSelectedItem();
        phongHocId = objPhongHoc.getId();
        
        //Lấy Id sinh viên
        IDSinhVien objIdSinhVien = (IDSinhVien) cboIdSV.getSelectedItem();
        maId = objIdSinhVien.getId();
        
        if (hoTen.length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Bạn cần phải nhập họ tên");
            txtHoTen.requestFocus();
            return;
        }
        if (strDiemThi.length() == 0){
            JOptionPane.showMessageDialog(rootPane, "Bạn cần phải nhập điểm thi");
            txtDiemThi.requestFocus();
            return;
        }
        diemThi = Double.parseDouble(strDiemThi);
        
        //Truyền dữ liệu vào đối tượng
        objDiemThi.setSinhVienId(maId);
        objDiemThi.setHoTen(hoTen);
        objDiemThi.setDiemThi(diemThi);
        objDiemThi.setNgayThi(ngayThi);
        objDiemThi.setMonHocId(monHocId);
        objDiemThi.setPhongHocId(phongHocId);
        
        boolean kq = false;
        if (!sinhVienId.isEmpty()) {
            kq = diemThiBus.capNhat(objDiemThi);
            
        } else {
            //Gọi hàm thêm mới
            kq = diemThiBus.themMoi(objDiemThi);
        }
        if (kq) {
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công");
        } 
        
        //Reload lại danh sách
        FrmTraCuuDiemThi.hienThiDanhSachDiem();
     
    }//GEN-LAST:event_btnCapNhatActionPerformed

