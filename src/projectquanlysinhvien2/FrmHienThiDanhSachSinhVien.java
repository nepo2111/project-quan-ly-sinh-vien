    //Hiển thị khoa lên JList
    public static void hienThiDanhSachKhoa(){
        ChuyenKhoaBusiness chuyenKhoaBus = new ChuyenKhoaBusiness();
        //Lấy danh sách khoa
        List<ChuyenKhoa> lstChuyenKhoa = chuyenKhoaBus.layDanhSachChuyenKhoa();
        //Khai báo model để truyền lên jList
        DefaultListModel model = new DefaultListModel();
        
        //Duyệt list để truyền vào model
        for (ChuyenKhoa o : lstChuyenKhoa) {
            model.addElement(o);         
        }
        //Hiển thị lên jList
        jListKhoa.setModel(model);
        //Tùy biến lại từng item của jList
        jListKhoa.setCellRenderer(new KhoaRenderJlist());
    }
