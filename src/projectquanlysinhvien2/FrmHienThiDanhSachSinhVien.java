    public static void locSinhVienTheoKhoa(List<ChuyenKhoa> lstCk) {
        //Khai báo cột tiêu đề
        String[] cotTieuDe = new String[]{"ID", "Mã SV", "Họ Tên", "Giới Tính", "Ngày Sinh", "Điện Thoại" , "Email"
        , "Địa Chỉ", "Chuyên Khoa ID"};
        //Khai báo model
        DefaultTableModel model = new DefaultTableModel(cotTieuDe, 0);
        //Khai báo đối tượng để gọi hàm hiển thị danh sách
        SinhVienBusiness sinhVienBus = new SinhVienBusiness();
        //Gọi hàm hiển thị danh sách
        List<SinhVien> lstSinhVien = sinhVienBus.timKiemSinhVien(lstCk);

        Object row[];
        for (SinhVien sv : lstSinhVien) {
            row = new Object[9];
            row[0] = sv.getId();
            row[1] = sv.getMaSV();
            row[2] = sv.getHoTen();
            row[3] = sv.getGioiTinh();
            row[4] = sv.getNgaySinh();
            row[5] = sv.getDienThoai();
            row[6] = sv.getEmail();
            row[7] = sv.getDiaChi();
            row[8] = sv.getChuyenKhoaId();

            model.addRow(row);
        }
        jTableDanhSach.setModel(model);
    }
